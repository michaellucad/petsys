SELECT
  BILLADDR1,
  BILLADDR2,
  BILLCITY,
  BILLCOUNTRY,
  BILLSTATE,
  BILLTOFIRSTNAME,
  BILLTOLASTNAME,
  BILLZIP,
  SHIPADDR1,
  SHIPADDR2,
  SHIPCITY,
  SHIPCOUNTRY,
  SHIPSTATE,
  SHIPTOFIRSTNAME,
  SHIPTOLASTNAME,
  SHIPZIP,
  CARDTYPE,
  COURIER,
  CREDITCARD,
  EXPRDATE,
  LOCALE,
  ORDERDATE,
  ORDERS.ORDERID,
  TOTALPRICE,
  USERID,
  STATUS
FROM 
  ORDERS, 
  ORDERSTATUS
WHERE 
  ORDERS.USERID = /*username*/'username'
  AND 
  ORDERS.ORDERID = ORDERSTATUS.ORDERID
ORDER BY 
  ORDERDATE